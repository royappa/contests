class Percents {
public:
	int minSamples(string percent) 
	{
		double x;
		sscanf(percent.c_str(), "%lf", &x);
		for (int d = 1; d <= 10000; d++)
		{
				int t = floor(d*x/100.0);
				for (int yes = t; yes <= t+1; yes++)
				{
					char buf[100];
					sprintf(buf, "%05.2lf%%", (double) yes*100.0/(double) d);
					string s = buf;
					cout << yes << " " << d << " " << s << endl;
					if (s == percent)
						return d;
				}
		}
	}
};


