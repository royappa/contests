class StreetParking {
public:
	int freeParks(string s) 
	{
		int pos = 0;
		int n = s.size();
		for (int i = 0; i < s.size(); i++)
		{
			if (s[i] != 'D' && s[i] != 'B' && !(i+1<n && s[i+1] == 'B') && !(i+2 < n && s[i+2] == 'B') 
				&& s[i] != 'S' && !(i+1 < n && s[i+1] == 'S') && !(i-1 >= 0 && s[i-1] == 'S'))
				pos++;
		}
		return pos;
	}
};


