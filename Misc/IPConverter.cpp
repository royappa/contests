class IPConverter {
public:
	vector <string> possibleAddresses(string);
};

vector <string> IPConverter::possibleAddresses(string s) 
{
	vector<string> results;
	
	int n = s.size();
	for (int i = 1; i <= n; i++)
	{
		for (int j = 1; j <= n; j++)
		{
			for (int k = 1; k <= n; k++)
			{
				if (i > 3 || j > 3 || k > 3 || (n-(i+j+k)) > 3 || (n-(i+j+k)) < 1)
					continue;
				string a = s.substr(0, i);
				if (a[0] == '0' && a != "0") continue; if (atoi(a.c_str()) > 255) continue;
				string b = s.substr(i, j);
				if (b[0] == '0' && b != "0") continue; if (atoi(b.c_str()) > 255) continue;
				string c = s.substr(i+j, k);
				if (c[0] == '0' && c != "0") continue; if (atoi(c.c_str()) > 255) continue;
				string d = s.substr(i+j+k, n-(i+j+k));
				if (d[0] == '0' && d != "0") continue; if (atoi(d.c_str()) > 255) continue;
				results.push_back(a+"."+b+"."+c+"."+d);
			}
		}
	}
	sort(results.begin(), results.end());
	return results;
}

