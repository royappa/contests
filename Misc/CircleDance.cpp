class CircleDance {
public:
	int arrangeDancers(vector <int>);
};

int CircleDance::arrangeDancers(vector <int> heights) {
	sort(heights.begin(), heights.end());
	int result = 0;
	for(int i = 2; i < heights.size(); ++i)
		result = max(result, heights[i] - heights[i-2]);
	return result;


	
}

