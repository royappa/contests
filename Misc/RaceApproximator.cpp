class RaceApproximator {
public:
	string timeToBeat(int, int, int, int, int);
};

string RaceApproximator::timeToBeat(int d1, int t1, int d2, int t2, int raceDistance) 
{
	int t = (int) (
		(double)t1*(double)exp(log((double)t2/(double)t1)*log((double)d1/(double)raceDistance)/log((double)d1/(double)d2))
		);
	char buf[100];
	sprintf(buf, "%d:%02d:%02d", t/3600, (t%3600)/60, t%60);
	return string(buf);
}

