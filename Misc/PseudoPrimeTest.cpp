class PseudoPrimeTest {
public:
	int firstFail(int);
	int powMod(int b, int power, int modulus)
	{
		if (power == 0)
		{
			return 1;
		}
		else if (power == 1)
		{
			return b % modulus;
		}
		int p1 = power / 2;
		int p2 = power % 2;
		long ret = powMod(b, p1, modulus)%modulus;
		ret = (ret * ret) % modulus;
		return p2 == 1 ? (ret*b)%modulus : ret;
	}
};

int PseudoPrimeTest::firstFail(int q) 
{
	for (int b = 2; b <= q-2; b++)
	{
		if (powMod(b, q-1, q) != 1)
			return b;
	}	
	return q;
}

