typedef long long LL;

class CardCosts {
public:

long long mincost(int n, int _k);

};


LL k;

LL points(int c, int r)
{
	LL pow = 1;
	while (r > 0)
	{
		pow *= k;
		r--;
	}
	return pow*c*c;
}

LL cost(int n, int r)
{
cout << n << " " << r << endl;
	if (n <= 0)
	{
		return 0LL;
	}
	
	LL best = points(n, r);	
	for (int p = 0; p < n; p++)
	{
		if (points(p, r+1) > points(n-p, r))
			continue;
		LL cur = points(p, r) + cost(n-p, r+1);
		best = min(best, cur);
	}
	
	return best;	
}


long long CardCosts::mincost(int n, int _k) 
{
	k = _k;	
	return cost(n, 1);
}


