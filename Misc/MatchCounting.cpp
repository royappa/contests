typedef long long LL;

LL p10(int x)
{
	LL y = 1LL;
	for (int i = 0; i < x; i++)
	{
		y *= 10LL;
	}
	return y;
}		

long long MatchCounting::count(int n) 
{
	LL A[150][10];
	
	int val[] = { 6, 2, 5, 5, 4, 5, 6, 4, 7, 6 };
	
	for (int d = 0; d <= 9; d++)
	{
		A[1][d] = 0LL;
	}
	for (int i = 2; i <= n; i++)
	{
		for (int d = 0; d <= 9; d++)
		{
			// compute biggest A[i-val[d]][...]
			LL m = 0LL;
			for (int p = 0; p <= 9; p++)
			{
				if (i-val[d] >= 1 && A[i-val[d]][p] > m)
				{
					m = A[i-val[d]][p];
				}
			}	
			int numd = 0;
			LL g = m;
			while (g > 0)
			{
				numd++;
				g /= 10LL;
			}
			LL pp = p10(numd);		
			LL x = pp*((LL) d)+m;
		}
	}	
	// when done
	LL m = 0LL;
	for (int d = 0; d <= 9; d++)
	{
		if (A[n][d] > m)
		{
			m = A[n][d];
		}
	}
	return m+1LL;
}

