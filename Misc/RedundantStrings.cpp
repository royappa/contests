class RedundantStrings {
public:
	int howMany(int);
};

int RedundantStrings::howMany(int length) 
{

	if (length == 1)
		return 0;
		
	int total = 0;
		
	for (int sub = 1; sub <= length/2; sub++)
	{
		if (length%sub != 0 || length/sub < 2)
			continue;
		// how many strings of length sub? 2^sub
		total += (1<<sub) - howMany(sub);
	}
	return total;	
}

