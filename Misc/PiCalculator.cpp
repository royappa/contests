class PiCalculator {
public:
string calculate(int p) {
	string pi = "3.141592653589793238462643383279";
	string pp = pi.substr(0, 2+p+1);
	int len = pp.size();
	int d = pp[len-1]-'0';
	if (d < 5)
		return pp.substr(0,len-1);
	pp = pp.substr(0, len-1);
	int k = pp.size()-1;
	int carry = 1;
	do 
	{
		if (pp[k] == '9')
		{
			pp[k] = '0';
			carry = 1;
		}
		else
		{
			pp[k]++;
			carry = 0;
		}
		k--;
	}
	while (carry > 0);
	return pp;
}
};


