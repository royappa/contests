using namespace std;

class MassiveNumbers {
public:
string getLargest(string a, string b) 
{
	int ab,ae, bb, be;
	sscanf(a.c_str(), "%d^%d", &ab, &ae);
	sscanf(b.c_str(), "%d^%d", &bb, &be);
	return ae*log((double)ab) > be*log((double)bb) ? a : b;
}
};


