class TeamBuilder {
public:
	vector <int> specialLocations(vector <string>);
};

vector <int> TeamBuilder::specialLocations(vector <string> paths) 
{
	int A[100][100];
	
	int N = paths.size();
	for (int i = 0; i < N; i++)
	{
		for (int j = 0; j < N; j++)
		{
			A[i][j] = paths[i][j]-'0';
		}
		A[i][i] = 1;
	}
	
	for (int k = 0; k < N; k++)
	{
		for (int i = 0; i < N; i++)
		{
			for (int j = 0; j < N; j++)
			{
				A[i][j] = A[i][j] || (A[i][k] && A[k][j]);

			}
		}
	}
	int cReaches=0, cReachable = 0;
	
	for (int i = 0; i < N; i++)
	{
		int c = 0, d = 0;
		for (int j = 0; j < N; j++)
		{
			if (A[i][j])
				c++;
			if (A[j][i])
				d++;
		}
		if (c == N)
			cReaches++;
		if (d == N)
			cReachable++;
	}
	vector<int> r;
	r.push_back(cReaches);
	r.push_back(cReachable);
	return r;
}

