class MathContest {
public:
int countBlack(string orig, int rep) 
{
	string s = "";
	for (int i = 0; i < rep; i++)
	{
		s += orig;
	}
	int n = s.size();
	int color = 0, dir = 0;
	int numBlack = 0;
	int cur = 0;
	n--;
	while (cur <= n)
	{
		char top = dir == 0 ? s[cur] : s[n];
		if (color == 1)
		{
			top = top == 'B' ? 'W' : 'B';
		}
		if (top == 'B')
			numBlack++;
		if (dir == 0)
		{
			cur++;
		}
		else
		{
			n--;
		}
		if (top == 'W')
		{
			dir = 1-dir;
		}
		else
		{
			color = 1-color;
		}
	}
	return numBlack;
}
};


