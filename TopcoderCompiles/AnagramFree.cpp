class AnagramFree {
public:
int getMaximumSubset(vector <string> v) 
{
	for (int i = 0; i < v.size(); i++)
	{
		sort(v[i].begin(), v[i].end());
	}
	sort(v.begin(), v.end());
	v.erase(unique(v.begin(), v.end()), v.end());
	return v.size();
}
};


