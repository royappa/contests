class MinimumLiars {
public:
bool possibleLiars(vector<int> v, int k)
{
	int n = v.size();
	// if there are k liars, there must be exactly k answers that are > k
	int bigger = 0;
	for (int i = 0; i < n; i++)
	{
		if (v[i] > k)
		{
			bigger++;
		}
	}
	return bigger == k;

}
int getMinimum(vector <int> v)
{
	for (int k = 0; k <= v.size(); k++)
	{
		if (possibleLiars(v, k))
		{
			return k;
		}
	}
	return -1;
}
};


