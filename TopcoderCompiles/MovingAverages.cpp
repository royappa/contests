class MovingAverages {
public:
vector <int> calculate(vector <string> v, int n) 
{
	vector<int> r;
	int m = v.size();	
	for (int i = 0; i+n <= m; i++)
	{
		int t = 0;
		for (int j = i; j < i+n; j++)
		{
			int a, b, c;
			sscanf(v[j].c_str(), "%d:%d:%d", &a, &b, &c);
			t += a*3600+b*60+c;
		}
		r.push_back(t/n);
	}
	return r;
}
};


