class FoxAndClassroom {
public:
string ableTo(int n, int m) 
{
	for (int sr = 0; sr < n; sr++)
	{
		for (int sc = 0; sc < m; sc++)
		{
			int i = sr, j = sc;
			int k = 0;
			do
			{
				i = (i+1)%n;
				j = (j+1)%m;
				k++;
			}
			while (!(i == sr && j == sc));
			if (k == n*m) return "Possible";
		}
	}
	return "Impossible";
}
};


