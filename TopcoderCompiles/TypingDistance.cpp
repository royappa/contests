class TypingDistance {
public:

int pos(string x, char c)
{
	for (int i = 0; i < x.size(); i++)
	{
		if (x[i] == c)
			return i;
	}
	return 0;
}

	
int minDistance(string kb, string w) 
{
	int d = 0;
	int p = pos(kb, w[0]);	
	for (int i = 1; i < w.size(); i++)
	{
		d += abs(p-pos(kb,w[i]));
		p = pos(kb, w[i]);
	}
	return d;
}
};


