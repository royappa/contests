class FoxAndHandleEasy {
public:
string isPossible(string S, string T) 
{
	int n = T.size();
	for (int i = 0; i < n; i++)
	{
		for (int j = i; j < n; j++)
		{
			string t = T;
			string t1 = T.substr(i, j-i+1);
			t.erase(t.begin()+i, t.begin()+i+(j-i+1));
			if (t == S && t1 == S)
				return "Yes";
		}
	}
	return "No";	
}
};


