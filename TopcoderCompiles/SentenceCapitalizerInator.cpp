class SentenceCapitalizerInator {
public:
string fixCaps(string s) 
{
	int n = s.size();
	for (int i = 0; i < n; i++)
	{
		if (i == 0 && s[i] != ' ')
		{
			s[i] = toupper(s[i]);
			continue;
		}
		if (i == 1 && s[i] == ' ' && s[i] != ' ')
		{
			s[i] = toupper(s[i]);
			continue;
		}
		if (i > 1 && s[i-1] == ' ' && s[i-2] == '.' && s[i] != ' ')
		{
			s[i] = toupper(s[i]);
			continue;
		}
	}
	return s;	
}
};


