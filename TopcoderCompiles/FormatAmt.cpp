class FormatAmt {
public:
string amount(int d, int c) 
{
	char buf[1000];
	if (d == 0)
	{
		sprintf(buf, "$0.%02d", c);
		return string(buf);
	}
	else
	{
		vector<int> v;
		while (d > 0)
		{
			v.push_back(d%1000);
			d /= 1000;
		}
		reverse(v.begin(), v.end());
		string s = "$";
		int n = v.size();
		char buf[100];
		for (int i = 0; i < n; i++)
		{
			if (i > 0)
			{
				sprintf(buf, "%03d", v[i]);
			}
			else
			{
				sprintf(buf, "%d", v[i]);
			}
			if (i != n-1)
			{
				s += string(buf)+",";
			}
			else
			{
				s += string(buf);
			}
		}
		sprintf(buf, ".%02d", c);
		s += string(buf);		
		return s;
	}
	return "";
			

}
};


