class OneDimensionalRobotEasy {
public:
int finalPosition(string s, int A, int B) 
{
	int n = s.size();	
	int p = 0;
	for (int i = 0; i < n; i++)
	{
		if (s[i] == 'L' && p > -A) p--;
		if (s[i] == 'R' && p < B) p++;
	}
	return p;
}
};


