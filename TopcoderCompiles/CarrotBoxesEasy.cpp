class CarrotBoxesEasy {
public:
int theIndex(vector <int> carrots, int K) 
{
	int last = 0;
	while (accumulate(carrots.begin(), carrots.end(), 0) > 0 && K > 0)
	{
		int mi = 0;
		for (int i = 0; i < carrots.size(); i++)
		{
			if (carrots[i] > carrots[mi])
			{
				mi = i;
			}
		}
		last = mi;
		K--;
		carrots[mi]--;
	}
	return last;
}
};


