class TheProgrammingContestDivTwo {
public:
vector <int> find(int T, vector <int> v) 
{
	sort(v.begin(), v.end());
	int t = 0;
	int n = v.size();
	int i = 0;
	int p = 0;
	for (i = 0; i < n; i++)
	{
		if (t+v[i] > T)
		{
			break;
		}
		t += v[i];
		p += t;
	}
	vector<int> w;
	w.push_back(i);
	w.push_back(p);
	return w;
}
};


