class LetterStrings {
public:
int sum(vector <string> s) 
{
	int n = s.size();
	int len = 0;
	for (int i = 0; i < n; i++)
	{
		for (int j = 0; j < s[i].size(); j++)
		{
			if (s[i][j] == '-') continue;
			len++;
		}
	}
	return len;	
}
};


