class CutoffRounder {
public:
int round(string num, string cutoff) 
{
	int p = num.find('.');
	string w = num.substr(0, p);
	string f = num.substr(p+1);
	cutoff = cutoff.substr(2);
	
	int r = 0;
	if (w == "")
	{
		r = 0;
	}
	else
	{
		sscanf(w.c_str(), "%d", &r);
	}
	while (f.size() < cutoff.size())
	{
		f += "0";
	}
	for (int i = 0; i < f.size(); i++)
	{
		if (f[i] < cutoff[i])
		{
			return r;
		}
		else if (f[i] > cutoff[i])
		{
			return r+1;
		}
	}
	return r;	
}
};


