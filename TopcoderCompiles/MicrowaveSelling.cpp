class MicrowaveSelling {
public:
int mostAttractivePrice(int minPrice, int maxPrice) 
{
	int m = 0, n;
	for (int i = minPrice; i <= maxPrice; i++)
	{
		int t = 0;
		int number = i;
		while (number%10 == 9)
		{
			t++;
			number /= 10;
		}
		if (t >= m)
		{
			m = t;
			n = i;
		}
	}
	return n;	
}
};


