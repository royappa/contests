class GardenHose {
public:

bool cant(int n, int pr, int pc, int rd, int cd, int hd)
{
	return hd < min(pr*rd+rd, (n-1-pr)*rd+rd) && hd < min(pc*cd+cd, (n-1-pc)*cd+cd);
}

int countDead(int n, int rd, int cd, int hd) 
{
	int r = 0;
	for (int pr = 0; pr < n; pr++)
	{
		for (int pc = 0; pc < n; pc++)
		{
			if (cant(n, pr, pc, rd, cd, hd))
				r++;
		}
	}
	return r;	
}
};


