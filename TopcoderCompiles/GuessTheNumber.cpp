class GuessTheNumber {
public:
int noGuesses(int upper, int answer) 
{
	int g = 0, lower = 1;
	do
	{
		g++;
		int x = (lower+upper)/2;
		if (x == answer) break;
		if (x < answer) lower = x+1; else upper = x-1;
	}
	while (1);
	return g;
}
};


