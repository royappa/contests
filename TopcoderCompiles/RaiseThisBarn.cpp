class RaiseThisBarn {
public:
int calc(string s) 
{
	int n = s.size();
	int r = 0;
	for (int k = 1; k <= n-1; k++)
	{
		int c1 = 0, c2 = 0;
		for (int i = 0; i < k; i++)
		{
			if (s[i] == 'c') c1++;
		}
		for (int i = k; i < n; i++)
		{
			if (s[i] == 'c') c2++;
		}
		if (c1 == c2) r++;
	}
	return r;
}
};


