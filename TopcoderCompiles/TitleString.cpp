class TitleString {
public:
string toFirstUpperCase(string s) 
{
	for (int i = 0; i < s.size(); i++)
	{
		if (islower(s[i]) && (i == 0 || !isalpha(s[i-1])))
			s[i] = toupper(s[i]);
	}
	return s;		
}
};


