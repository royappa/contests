class AccessChanger {
public:

string f(string s)
{
	int n = s.size();
	string t = "";
	for (int i = 0; i < n; i++)
	{
		if (s[i] == '/' && i <= n-2 && s[i+1] == '/')
		{
			return t+s.substr(i);
		}
		if (s[i] == '-' && i <= n-2 && s[i+1] == '>')
		{
			t += ".";
			i++;
			continue;
		}
		t += s[i];
	}
	return t;
}

vector <string> convert(vector <string> v) 
{
	for (int i = 0; i < v.size(); i++)
	{
		v[i] = f(v[i]);
	}
	return v;	
}
};


