#define INF (1<<30)
using namespace std;

class WordParts {
public:

string orig;
map<string,int> memo;

int f(string s)
{
	if (memo.count(s) > 0)
		return memo[s];
		
	if (s.size() == 0)
		return 0;
	if (s == orig)
		return 1;
		
	int r = INF;
	for (int len = 1; len <= orig.size(); len++)
	{
		string pre = orig.substr(0, len);
		if (s.size() >= pre.size() && s.substr(0, pre.size()) == pre)
			r = min(r, 1+f(s.substr(pre.size())));
	}
	for (int len = 1; len <= orig.size(); len++)
	{
		string suff = orig.substr(len);
		if (s.size() >= suff.size() && s.substr(0, suff.size()) == suff && s.substr(suff.size()) != s)
			r = min(r, 1+f(s.substr(suff.size())));
	}	
	return memo[s] = r;
}

int partCount(string original, string compound) 
{
	orig = original;
	int r = f(compound);
	return r >= INF ? -1 : r;	
}
};


