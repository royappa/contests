class MagicalGirlLevelOneDivTwo {
public:
double theMinDistance(int d, int x, int y) 
{
	if (x < 0) x = -x;
	if (y < 0) y = -y;
	x = max(x-d, 0);
	y = max(y-d, 0);
	return sqrt(x*x+y*y);
}
};


