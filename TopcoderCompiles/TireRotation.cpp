class TireRotation {
public:
int getCycle(string ini, string cur) 
{
	int p = 1;
	do
	{
		if (ini == cur) return p;
		string x = ini;
		x[3-1] = ini[1-1];
		x[2-1] = ini[3-1];
		x[4-1] = ini[2-1];
		x[1-1] = ini[4-1];
		ini = x;
		p++;
		if (p == 5) return -1;
	}
	while (1);
		
}
};


