class Filtering {
public:

bool check(int A, int B, vector<int> sizes, string outcome)
{
	for (int i = 0; i < sizes.size(); i++)
	{
		if (outcome[i] == 'A' && (sizes[i] < A || sizes[i] > B)) return false;
		else if (outcome[i] == 'R' && (A <= sizes[i] && sizes[i] <= B)) return false;
	}
	return true;
}

vector <int> designFilter(vector <int> sizes, string outcome) 
{
	int m = 1000;
	int mA = -1, mB = -1;
	for (int A = 1; A <= 100; A++)
	{
		for (int B = A; B <= 100; B++)
		{
			if (check(A, B, sizes, outcome))
			{
				if (B-A < m)
				{
					mB = B;
					mA = A;
					m = B-A;
				}
			}
		}
	}
	vector<int> v;
	if (mB != -1)
	{
		v.push_back(mA);
		v.push_back(mB);
	}
	return v;
}
};


