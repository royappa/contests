	LuckyRemainder * obj = new LuckyRemainder();
	clock_t start = clock();
	int my_answer = obj->getLuckyRemainder(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
