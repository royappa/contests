class DivisorDigits {
public:
int howMany(int number) 
{
	int t = 0;
	int o = number;
	while (number > 0)
	{
		int d = number%10;
		if (d && o%d == 0)
			t++;
		number /= 10;
	}
	return t;
 	
}
};


