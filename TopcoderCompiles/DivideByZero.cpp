class DivideByZero {
public:
int CountNumbers(vector <int> v) 
{
	int n = v.size();
	if (n == 1)
		return 1;
	sort(v.begin(), v.end());
	while (true)
	{
		sort(v.begin(), v.end());
		n = v.size();
		for (int i = 0; i < n; i++)
		{
			for (int j = i+1; j < n; j++)
			{
				if (v[j] > v[i] && find(v.begin(), v.end(), v[j]/v[i]) == v.end())
				{
					v.push_back(v[j]/v[i]);
					goto outer;
				}
			}
		}
		break;
outer:
		continue;
	}
	return v.size();
		
}
};


