class DiamondHunt {
public:
int countDiamonds(string mine) 
{
	int d = 0;
	int p;
	while ((p = mine.find("<>")) != string::npos)
	{
		mine.erase(mine.begin()+p, mine.begin()+p+2);
		d++;
	}
	return d;
}
};


