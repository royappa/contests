class DigitHoles {
public:
int numHoles(int number) 
{
	int h[] = {1,0,0,0,1,0,1,0,2,1};
	int t = 0;
	while (number > 0)
	{
		t += h[number%10];
		number /= 10;
	}
	return t;	
}
};


