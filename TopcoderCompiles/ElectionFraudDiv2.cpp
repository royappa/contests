class ElectionFraudDiv2 {
public:
string IsFraudulent(vector <int> per) 
{
	int n = per.size();
	int TOTAL = 10000, ls = 0, hs = 0;
	for (int i = 0; i < n; i++)
	{
		int p = per[i];
		int low = -1, high = -1;
		for (int x = 0; x <= TOTAL; x++)
		{
					
			if ((x*100+TOTAL/2)/TOTAL == p)
			{
				if (low == -1) low = x;
				high = x;
			}
		}
		if (low == -1) return "YES";
		ls += low;
		hs += high;
	}
	return ls <= TOTAL && TOTAL <= hs ? "NO" : "YES";
}
};


