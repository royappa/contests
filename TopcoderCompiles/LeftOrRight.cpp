class LeftOrRight {
public:

int reach(string s)
{
	int p = 0, r = 0;
	for (int i = 0; i < s.size(); i++)
	{
		if (s[i] == 'L') p--; else p++;
		r = max(r, abs(p));
	}
	return r;
}

int maxDistance(string s) 
{
	int l, r;
	string ss = s;
	for (int i = 0; i < s.size(); i++)
	{
		if (s[i] == '?') s[i] = 'L';
	}
	l = reach(s);
	s = ss;
	for (int i = 0; i < s.size(); i++)
	{
		if (s[i] == '?') s[i] = 'R';
	}
	r = reach(s);
	return max(l,r);
}
};


