class NoOrderOfOperations {
public:
int evaluate(string s) 
{
	int r = s[0]-'0';
	int n = s.size();
	for (int i = 1; i < n; i++)
	{
		if (!isdigit(s[i])) continue;
		int d = s[i]-'0';
		if (s[i-1] == '+') r += d;
		else if (s[i-1] == '-') r -= d;
		else r *= d;
	}
	return r;
}
};


