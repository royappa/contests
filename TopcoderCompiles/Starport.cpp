class Starport {
public:

typedef long long LL;

LL gcd(LL a, LL b)
{
	return b == 0 ? a : gcd(b, a %b);
}

double getExpectedTime(int N, int M) 
{
	LL g = N/gcd(N, M)*M;
	double t = 0;
	int n = 0;
	for (LL i = 0; i*M <= g; i++)
	{
		LL k = i*M;
		// find the multiple of N that is >= k
		// x*N >= k
		// x >= k/N
		// x = ceil(k/N) = (k+N-1)/N;
		LL x = (k+N-1)/N;
		t += x*N-k;
		n++;
	}
	cout << endl;
	return t/(n-1);
}
	
};


