class DinkyFish {
public:
int monthsUntilCrowded(int tankVolume, int m, int f) 
{
	int months = 0;
	int couples = min(m, f);
	m -= couples;
	f -= couples;
	while (tankVolume*2 >= (m+f+2*couples))
	{
		months++;
		m += couples;
		f += couples;
		int newcouples = min(m, f);
		m -= newcouples;
		f -= newcouples;
		couples += newcouples;
	}
	return months;
}
};


