class ErasingCharacters {
public:
string simulate(string s) 
{
	string t = "";
	while (s != t)
	{
		t = s;
		int i;
		int n = s.size();
		for (i = 0; i < n-1; i++)
		{
			if (s[i] == s[i+1])
			{
				s.erase(s.begin()+i, s.begin()+i+2);
				break;
			}
		}
	}
	return s;
}
};


