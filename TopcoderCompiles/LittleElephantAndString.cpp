class LittleElephantAndString {
public:
int getNumber(string A, string B) 
{
	string a = A, b = B;
	sort(a.begin(), a.end());
	sort(b.begin(), b.end());
	if (a != b)
		return -1;
	int n = a.size();
	// find the longest suffix of B that is a subsequence of A
	int i = n, j = n;
	while (i >= 0)
	{
		if (A[i] == B[j])
		{
			i--;
			j--;
		}
		else
		{
			i--;
		}
	}
	return j+1;
}
};


