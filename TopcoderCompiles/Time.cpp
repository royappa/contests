using namespace std;

class Time {
public:
string whatTime(int seconds) 
{
	int h = seconds/3600;
	int m = (seconds%3600)/60;
	int s = (seconds%3600)%60;
	char buf[100];
	sprintf(buf, "%d:%d:%d", h, m, s);
	return string(buf);
}
};


