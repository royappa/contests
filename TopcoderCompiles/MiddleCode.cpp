class MiddleCode {
public:
string encode(string s) 
{

	if (s.size() == 0) return "";
	int n = s.size();
	int p;
	if (n % 2 != 0)
	{
		p = n/2;
	}
	else
	{
		if (s[n/2-1] < s[n/2])
		{
			p = n/2-1;
		}
		else
			p = n/2;
	}
	char ch = s[p];
	s.erase(s.begin()+p);	
	return ch+encode(s);	
}
};


