class AccessLevel {
public:
string canAccess(vector <int> rights, int m) 
{
	string s = "";
	for (int i = 0; i < rights.size(); i++)
	{
		if (rights[i] >= m) 
			s += "A";
		else
			s += "D";
	}
	return s;
}
};


