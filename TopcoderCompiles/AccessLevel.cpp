	AccessLevel * obj = new AccessLevel();
	clock_t start = clock();
	string my_answer = obj->canAccess(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p2 = "DDAAAA";
	cout <<"Desired answer: " <<endl;
