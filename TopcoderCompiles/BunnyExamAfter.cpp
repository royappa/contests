class BunnyExamAfter {
public:
int getMaximum(string black, string grey, string white) 
{
	int r = 0;
	for (int i = 0; i < black.size(); i++)
	{
		if (grey[i] == white[i] && grey[i] != black[i]) r += 2;
		else if (grey[i] != black[i]) r++;
		else if (white[i] != black[i]) r++;
	}
	return r;
}
};


