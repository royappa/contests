class LuckyCounter {
public:
int countLuckyMoments(vector <string> moments) 
{
	int r = 0;
	for (int i = 0; i < moments.size(); i++)
	{
		string s =  moments[i];
		char x = s[0], y = s[1], z = s[3], w = s[4];
		if ((x == z && y == w) || (x == y && z == w) || (x == w && y == z))
			r++;
	}
	return r;
}
};


