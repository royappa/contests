class SimpleCalculator {
public:
int calculate(string s) 
{
	string ops = "+-*/";
	for (int i = 0; i < 4; i++)
	{
		char c = ops[i];
		if (s.find(c) != -1)
		{
			int p = s.find(c);
			string l = s.substr(0, p);
			string r = s.substr(p+1);
			int nl = atoi(l.c_str());
			int nr = atoi(r.c_str());
			return 
					c == '+' ? nl+nr :
				   	c == '-' ? nl-nr :
				   	c == '*' ? nl*nr : nl/nr;
		}
	}
}
};


