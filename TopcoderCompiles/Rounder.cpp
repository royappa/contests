class Rounder {
public:
int round(int n, int b) 
{
	if (n%b == 0) return (n/b)*b;
	if (b%2 == 0)
	{
		if (n%b == b/2) return (n+b-1)/b*b;
		return n%b < b/2 ? n/b*b : (n+b-1)/b*b;
	}
	return n%b <= b/2 ? n/b*b : (n+b-1)/b*b;
}
};


