class FauxPalindromes {
public:

bool isPal(string s)
{
	int n = s.size();
	for (int i = 0; i < n/2; i++)
	{
		if (s[i] != s[n-i-1])
			return false;
	}
	return true;
}

string classifyIt(string word) 
{
	if (isPal(word)) return "PALINDROME";
	string t = "";
	t += word[0];
	int n = word.size();
	for (int i = 1; i < n; i++)
	{
		if (word[i] == word[i-1])
			continue;
		t += word[i];
	}
	if (isPal(t)) return "FAUX";
	return "NOT EVEN FAUX";		
}
};


