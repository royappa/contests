class Chivalry {
public:
string getOrder(string s1, string s2) 
{
	string r = "";
	while (s1.size() > 0 && s2.size() > 0)
	{
		if (s1[0] == 'W' && s2[0] == 'W')
		{
			r += "W";
			s1 = s1.substr(1);
		}
		else if (s1[0] == 'W' && s2[0] == 'M')
		{
			r += "W";
			s1 = s1.substr(1);
		}
		else if (s1[0] == 'M' && s2[0] == 'W')
		{
			r += "W";
			s2 = s2.substr(1);
		}
		else
		{
			r += "M";
			s1 = s1.substr(1);
		}	
	}
	return r+s1+s2;
}
};


