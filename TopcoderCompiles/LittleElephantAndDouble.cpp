class LittleElephantAndDouble {
public:
bool dub(int y, int x)
{
//cout << "*" << y << " " << x << endl;
	while (y < x)
	{
		y *= 2;
	}
//cout << "+" << y << " " << x << " " << (x==y) << endl;
	return x==y;
}

string getAnswer(vector <int> v) 
{
	int n = v.size();
	for (int i = 0; i < n; i++)
	{
		int x = v[i];
		bool all = true;
		for (int j = 0; j < n; j++)
		{
			if (!dub(v[j], x))
			{
				all = false;
			}
			//cout << v[j] << " " << x << " = " << all << endl;
		}
//cout << x << all << endl;
		if (all)
			return "YES";
	}
	return "NO";
}
};


