class BlackWhiteMagic {
public:
int count(string s)
{
	int n = s.size();
	int w = 0, b = 0;
	int last = -1, first = -1;
	for (int i = 0; i < n; i++)
	{
		if (s[i] == 'W')
		{
			last = i;
			w++;
		}
		else if (first == -1)
		{
			first = i;
		}
	}
	b = n-w;
	if (w == n || b == n)
		return 0;
	int num = 0;
//cout << first << " " << last << endl;
	while (first < last)
	{
		num++;
//cout << num << endl;
		bool flag = false;
		bool x = false, y = false;
		for (int i = last-1; i > first; i--)
		{
			if (s[i] == 'W')
			{
				last = i;
				x = true;
				break;
			}
		}
		for (int i = first+1; i < last; i++)
		{
			if (s[i] == 'B')
			{
				first = i;
				y = true;
				break;
			}
		}
		//cout << first << " " << last << " " << x << " " << y << endl;
		if (!x || !y)
			break;
	}
	return num;

}
};


