class PalindromizationDiv2 {
public:
bool isPal(int x)
{
	if (x < 0) return false;
	char buf[100];
	sprintf(buf, "%d", x);
	string s(buf);
	string t = s;
	reverse(t.begin(), t.end());
	return s == t;
}

int getMinimumCost(int X) 
{
	for (int i = 0; ; i++)
	{
		if (isPal(X+i) || isPal(X-i)) return i;
	}
	return 0;	
}
};


