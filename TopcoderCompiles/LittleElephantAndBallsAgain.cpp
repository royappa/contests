class LittleElephantAndBallsAgain {
public:
int getNumber(string s) 
{
	int n = s.size();
	int len = 1;
	int best = 1;
	for (int i = 1; i < n; i++)
	{
		if (s[i] != s[i-1])
		{
			best = max(best, len);
			len = 1;
		}
		else
		{
			len++;
		}
	}
	best = max(best, len);
	return n-best;
}
};


