class WolfDelaymaster {
public:
string check(string s) 
{
	int n = s.size();	
	vector<string> v;
	for (int i = 1; i < 100; i++)
	{
		string w(i,'w'), o(i,'o'), l(i,'l'), f(i,'f');
		string t = w+o+l+f;
		if (t.size() > 50) break;
		v.push_back(t);
	}
//cout << v[0] <<  " " << v[1] << endl;
	while (s.size() > 0)
	{
		string k = "";
		for (int i = 0; i < v.size(); i++)
		{
			string t = v[i];
			if (s.size() >= t.size() && s.substr(0, t.size()) == t)
			{
				k = t;
				break;
			}
		}
		if (k == "")
			return "INVALID";
		s = s.substr(k.size());
	}
	return "VALID";
		
}
};


