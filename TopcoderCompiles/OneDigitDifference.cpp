class OneDigitDifference {
public:
int getSmallest(int N) 
{
	if (N == 0)
		return 1;
		
	char buf[30];
	sprintf(buf, "%020d", N);
	string s = buf;
	for (int i = 0; i < s.size(); i++)
	{
		if (s[i] != '0')
		{
			s[i] = '0';
			break;
		}
	}
	return atoi(s.c_str());	
}
};


