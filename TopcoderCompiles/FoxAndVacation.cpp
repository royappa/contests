class FoxAndVacation {
public:
int maxCities(int total, vector <int> d) 
{
	sort(d.begin(), d.end());
	int n = d.size();
	int t = 0;
	int i = 0;
	while (i < n && total >= d[i])
	{
		t++;
		total -= d[i];
		i++;
	}
	return t;
}
};


