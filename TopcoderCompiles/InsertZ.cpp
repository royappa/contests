class InsertZ {
public:
string canTransform(string a, string b) 
{
	int n = b.size();
	for (int i = n-1; i >= 0; i--)
	{
		if (b[i] == 'z')
			b.erase(b.begin()+i);
	}
	return a==b?"Yes":"No";
}
};


