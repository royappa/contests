class Workshop {
public:
int pictureFrames(vector <int> v) 
{
	int n = v.size();
	vector<int> x(3);
	
	int r = 0;
	for (int i = 0; i < n; i++)
	{
		for (int j = i+1; j < n; j++)
		{
			for (int k = j+1; k < n; k++)
			{
				x[0] = v[i];
				x[1] = v[j];
				x[2] = v[k];
				sort(x.rbegin(), x.rend());
				if (x[0] < x[1]+x[2])
					r++;
			}
		}
	}
	return r;					
}
};


