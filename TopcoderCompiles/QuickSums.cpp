class QuickSums {
public:
#define INF (1<<30)
map< pair<string,int>, int > memo;

int f(string s, int v)
{
	int sv = atoi(s.c_str());
	if (sv == v)
	{
		return 0;
	}
	if (sv <= 9)
	{
		return INF;
	}
	pair<string,int> key = make_pair(s, v);
	if (memo.count(key) > 0)
	{
		return memo[key];
	}
	int r = INF;
	for (int i = 0; i < int(s.size())-1; i++)
	{
		int nv = atoi(s.substr(i+1).c_str());
		int r2 = 1+f(s.substr(0, i+1), v-nv);
		r = min(r, r2);
	}
	return memo[key] = r;
}
	
	
int minSums(string numbers, int sum) 
{
	int r = f(numbers, sum);
	return r == INF ? -1 : r;
}

};


