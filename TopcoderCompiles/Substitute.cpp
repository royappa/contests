class Substitute {
public:
int getValue(string key, string code) 
{
	int num = 0;
	for (int i = 0; i < code.size(); i++)
	{
		char c = code[i];
		int p = key.find(c);
		if (p == -1) continue;
		p = (p+1)%10;
		num = num*10+p;
	}
	return num;
}
};


