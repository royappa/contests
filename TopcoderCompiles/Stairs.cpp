class Stairs {
public:

bool poss(int r, int t, int th, int tw)
{
	if (th % r != 0) return false;
	int nr = th / r;
	return (tw % t) == 0 && (tw / t) == nr-1;
}

int designs(int maxHeight, int minWidth, int totalHeight, int totalWidth) 
{
	int r = 0;
	for (int i = 1; i <= maxHeight; i++)
	{
		for (int j = minWidth; j <= totalWidth; j++)
		{
			if (poss(i, j, totalHeight, totalWidth))
			{
				r++;
			}
		}
	}
	return r;	
}

};


