class MiniatureDachshund {
public:
int maxMikan(vector <int> v, int weight) 
{
	int n = v.size();
	sort(v.begin(), v.end());
	int k = 0;
	while (k < n && weight+v[k] <= 5000)
	{
		weight += v[k];
		k++;
	}
	return k;
}
};


