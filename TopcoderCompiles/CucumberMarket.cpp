class CucumberMarket {
public:


string check(vector <int> price, int budget, int k) 
{
	int n = price.size();
	for (int i = 0; i < (1<<n); i++)
	{
		if (__builtin_popcount(i) == k)
		{
			int s = 0;
			for (int j = 0; j < n; j++)
			{
				if ((i & (1<<j)) != 0)
				{
					s += price[j];
				}
			}
			if (s > budget) return "NO";
		}
	}
	return "YES";	
}
};


