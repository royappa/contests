class Stick {
public:
int pieces(int x) 
{
	multiset<int> s;
	s.insert(64);
	while (accumulate(s.begin(), s.end(), 0) > x)
	{
		int z = *min_element(s.begin(), s.end());
		for (auto it = s.begin(); it != s.end(); it++)
			cout <<  "s  : " << *it << endl;
cout << z << endl;
		s.erase(min_element(s.begin(), s.end()));
		s.insert(z/2);
		if (accumulate(s.begin(), s.end(), 0) < x)
			s.insert(z/2);
		for (auto it = s.begin(); it != s.end(); it++)
			cout <<  "t  : " << *it << endl;			
	}
	return s.size();
}
};


