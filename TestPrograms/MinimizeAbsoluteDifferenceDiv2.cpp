class MinimizeAbsoluteDifferenceDiv2 {
public:

bool less(vector<int> s, vector<int> t, vector<int> v) {
	double xa = v[s[0]], xb = v[s[1]], xc = v[s[2]];
	double ya = v[t[0]], yb = v[t[1]], yc = v[t[2]];
	return abs(xa/xb-xc) < abs(ya/yb-yc);
}
vector <int> findTriple(int x0, int x1, int x2) {
	vector<int> v = {x0, x1, x2};
	vector<int> s = {0, 1, 2};
	vector<int> bestS = s;
	do {
		if (less(s, bestS, v)) {
			bestS[0] = s[0]; bestS[1] = s[1]; bestS[2] = s[2];
		}
	}
	while (next_permutation(s.begin(), s.end()));
	return bestS;	
}
};


