	private int gcd(int a, int b)
	{
		if (b == 0)
			return a;
		return gcd(b, a%b);
	}
	
	public int[] getDifferences(int n)
	{
		int s = 0, S = 0;
		for (int i = 1; i <= n-1; i++)
		{
			s += i*i*i;
		}
		S = s + n*n*n;
		
		int[] frac = new int[2];
		
		int num = (S+s)*4-2*(n*n*n*n);
		int den = (2*4);
		
		frac[0] = num/gcd(num, den);
		frac[1] = den/gcd(num, den);
		
		return frac;	
	}
	
