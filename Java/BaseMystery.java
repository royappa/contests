	private boolean valid(String s, int base)
	{
		for (int i = 0; i < s.length(); i++)
		{
			char ch = s.charAt(i);
			int val = 0;
			if (Character.isDigit(ch)) val = ch - '0'; else val = ch - 'A' + 10;
			if (val >= base) return false;
		}
		return true;	
	}
	
	private int convert(String s, int base)
	{
		int val = 0, p = 1;
		for (int i = s.length()-1; i >= 0; i--)
		{
			int dval = Character.isDigit(s.charAt(i)) ? s.charAt(i)-'0' : s.charAt(i)-'A'+10;
			val = dval*p+val;
			p = p*base;
		}
		//if (base==20) System.out.println("XX"+s+" "+base+" "+val);
		return val;
	
	}
	public int[] getBase(String equation)
	{
		String[] w = equation.split("[\\+=]");
		System.out.println("**"+w[0]+" "+w[1]+" "+w[2]);
		int[] bases = new int[21];
		int nb = 0;
		for (int base = 2; base <= 20; base++)
		{
			String a = w[0], b = w[1], c = w[2];
			if (!valid(a, base) || !valid(b, base) || !valid(c, base))
				continue;
			int da = convert(a, base), db = convert(b, base), dc = convert(c, base);
			if (base==20) System.out.println(da+" "+db+" "+dc);
			if (da+db != dc) continue;
			bases[nb++] = base;
		}
		int[] result = new int[nb];
		for (int i = 0; i < nb; i++) result[i] = bases[i];
		return result;
	}
	
