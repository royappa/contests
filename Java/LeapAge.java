	public int getAge(int year, int born)
	{
		int c  = 0;
		int a = Math.min(year, born);
		int b = Math.max(year, born);
		for (int y = a; y <= b; y++)
		{
			if (y == born) continue;
			if (y%4==0)
				if (y%100!=0 || y%400==0)
					c++;		
		}
		return c;
	}
	
