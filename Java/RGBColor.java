	public int[] getComplement(int[] rgb)
	{
		int a =0,b=0,c=0;
		if (Math.abs((255-rgb[0])-rgb[0]) <= 32 && Math.abs((255-rgb[1])-rgb[1]) <= 32   
			&& Math.abs((255-rgb[2])-rgb[2]) <= 32)
		{ 
			a = (rgb[0]-128 >= 0 ? rgb[0]-128 : rgb[0]+128);
			b = (rgb[1]-128 >= 0 ? rgb[1]-128 : rgb[1]+128);
			c = (rgb[2]-128 >= 0 ? rgb[2]-128 : rgb[2]+128);
		}
		else {
			a = 255-rgb[0];
			b = 255-rgb[1];
			c = 255-rgb[2];
		
		}
		int[] ret = { a, b, c };
		return ret;
	}
	
