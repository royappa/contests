	public int howMany(int[] line)
	{
		int c = 0;
		
		for (int i = 1; i < line.length; i++)
		{
			for (int j = 0; j < i; j++)
			{
				if (line[j] < line[i])
				{
					c++;
				}
			}
		}
		return c;
	}
	
